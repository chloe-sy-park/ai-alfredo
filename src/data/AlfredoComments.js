export function getStreakComment(streakDays) {
  const milestones = Object.keys(ACHIEVEMENT_COMMENTS.streakMilestone)
    .map(Number)
    .sort((a, b) => b - a);
  
  for (const milestone of milestones) {
    if (streakDays >= milestone) {
      return ACHIEVEMENT_COMMENTS.streakMilestone[milestone];
    }
  }
  return null;
}

