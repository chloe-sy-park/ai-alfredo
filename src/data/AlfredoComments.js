/**
 * ìº˜ë¦°ë” ìƒí™© ê¸°ë°˜ ì½”ë©˜íŠ¸ ê°€ì ¸ì˜¤ê¸°
 */
export function getCalendarComment(context) {
  const { meetingCount, isBeforeMeeting, isAfterMeeting, isBackToBack } = context;
  
  if (isBackToBack) {
    return getRandomMessage(CALENDAR_COMMENTS.backToBack);
  }
  if (isBeforeMeeting) {
    return getRandomMessage(CALENDAR_COMMENTS.beforeMeeting);
  }
  if (isAfterMeeting) {
    return getRandomMessage(CALENDAR_COMMENTS.afterMeeting);
  }
  if (meetingCount >= 3) {
    return getRandomMessage(CALENDAR_COMMENTS.busyDay);
  }
  if (meetingCount <= 1) {
    return getRandomMessage(CALENDAR_COMMENTS.lightDay);
  }
  return null;
}

